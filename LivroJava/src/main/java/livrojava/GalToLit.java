package livrojava;

/* probrama para converter galões em litros */

public class GalToLit {

	public static void main(String args[]) {
		
		double galoes;
		double litros;
		
		galoes = 10;  //declara valor da variável galão
		
		
		litros = galoes * 3.7854; //faz a conversão
		
		System.out.println(galoes + " galoes é " + litros + " litros.");
		
	}
	
	
}
